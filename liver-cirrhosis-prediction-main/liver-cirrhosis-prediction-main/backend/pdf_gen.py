import os
from datetime import datetime
from typing import Dict, List, Optional, Any
import logging
from pathlib import Path

from fpdf import FPDF
from database import get_db, DiagnosisRecord, User

logger = logging.getLogger(__name__)

# Reports directory always relative to this file (backend/reports/)
REPORTS_DIR = Path(__file__).parent / "reports"
REPORTS_DIR.mkdir(exist_ok=True)

class MedicalReportGenerator:
    """Generate medical reports in PDF format"""

    def __init__(self):
        self.logo_path = None  # Path to hospital/clinic logo
        self.footer_text = "This report is generated by AI system. Please consult healthcare professional for medical advice."

    async def generate_report(self, diagnosis_id: int, user_id: str) -> str:
        """Generate comprehensive medical report"""
        try:
            # Get diagnosis data
            diagnosis_data = await self._get_diagnosis_data(diagnosis_id, user_id)
            if not diagnosis_data:
                raise ValueError("Diagnosis record not found")

            # Create PDF
            pdf = self._create_pdf_report(diagnosis_data)

            # Save PDF
            filename = f"liver_report_{diagnosis_id}_{datetime.now().strftime('%Y%m%d_%H%M%S')}.pdf"
            filepath = str(REPORTS_DIR / filename)

            pdf.output(filepath)
            return filepath

        except Exception as e:
            logger.error(f"Report generation error: {str(e)}")
            raise

    async def _get_diagnosis_data(self, diagnosis_id: int, user_id: str) -> Optional[Dict[str, Any]]:
        """Retrieve diagnosis data from database"""
        try:
            db = next(get_db())

            # Get diagnosis record
            diagnosis = db.query(DiagnosisRecord).filter(
                DiagnosisRecord.id == diagnosis_id,
                DiagnosisRecord.user_id == user_id
            ).first()

            if not diagnosis:
                return None

            # Get user info
            user = db.query(User).filter(User.id == diagnosis.user_id).first()

            # Convert to dictionary
            diagnosis_dict = {
                'id': diagnosis.id,
                'user_id': diagnosis.user_id,
                'symptoms': diagnosis.symptoms,
                'prediction': diagnosis.prediction_result,
                'confidence': diagnosis.confidence_score,
                'explanation': diagnosis.explanations,
                'recommendations': diagnosis.recommended_actions,
                'created_at': diagnosis.created_at,
                'user': {
                    'name': user.full_name if user else 'Unknown',
                    'email': user.email if user else 'Unknown',
                    'age': None
                }
            }

            return diagnosis_dict

        except Exception as e:
            logger.error(f"Data retrieval error: {str(e)}")
            return None

    def _create_pdf_report(self, data: Dict[str, Any]) -> FPDF:
        """Create PDF report with diagnosis data"""

        class SafePDF(FPDF):
            """FPDF subclass that sanitises text to avoid Latin-1 encoding errors."""
            def cell(self, w, h=0, txt='', border=0, ln=0, align='', fill=False, link=''):
                safe = (txt or '').encode('latin-1', errors='replace').decode('latin-1')
                super().cell(w, h, safe, border, ln, align, fill, link)

            def multi_cell(self, w, h, txt='', border=0, align='J', fill=False):
                safe = (txt or '').encode('latin-1', errors='replace').decode('latin-1')
                super().multi_cell(w, h, safe, border, align, fill)

        pdf = SafePDF()
        pdf.add_page()

        # Set fonts
        pdf.set_font("Arial", "B", 16)

        # Header
        self._add_header(pdf, data)

        # Patient Information
        self._add_patient_info(pdf, data)

        # Diagnosis Results
        self._add_diagnosis_results(pdf, data)

        # Symptoms Analysis
        self._add_symptoms_analysis(pdf, data)

        # Recommendations
        self._add_recommendations(pdf, data)

        # Explanations
        self._add_explanations(pdf, data)

        # Footer
        self._add_footer(pdf)

        return pdf

    def _safe_text(self, text: str) -> str:
        """Strip non-latin characters to avoid fpdf2 encoding issues."""
        if not text:
            return ''
        return text.encode('latin-1', errors='replace').decode('latin-1')

    def _add_header(self, pdf: FPDF, data: Dict[str, Any]):
        """Add report header"""
        # Title
        pdf.set_font("Arial", "B", 20)
        pdf.cell(0, 15, "Liver Cirrhosis Diagnostic Report", 0, 1, "C")
        pdf.ln(5)

        # Subtitle
        pdf.set_font("Arial", "I", 12)
        pdf.cell(0, 10, "AI-Powered Non-Invasive Liver Assessment", 0, 1, "C")
        pdf.ln(5)

        # Report Info
        pdf.set_font("Arial", "", 10)
        pdf.cell(0, 8, f"Report ID: {data['id']}", 0, 1)
        pdf.cell(0, 8, f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}", 0, 1)
        pdf.cell(0, 8, f"Diagnosis Date: {data['created_at'].strftime('%Y-%m-%d %H:%M:%S')}", 0, 1)
        pdf.ln(10)

    def _add_patient_info(self, pdf: FPDF, data: Dict[str, Any]):
        """Add patient information section"""
        pdf.set_font("Arial", "B", 14)
        pdf.cell(0, 12, "Patient Information", 0, 1)
        pdf.ln(2)

        pdf.set_font("Arial", "", 11)
        user = data.get('user', {})

        # Create table-like structure
        pdf.cell(40, 8, "Name:", 0, 0)
        pdf.cell(0, 8, str(user.get('name', 'Not provided')), 0, 1)

        pdf.cell(40, 8, "Email:", 0, 0)
        pdf.cell(0, 8, str(user.get('email', 'Not provided')), 0, 1)

        pdf.cell(40, 8, "Age:", 0, 0)
        pdf.cell(0, 8, str(user.get('age', 'Not provided')), 0, 1)

        pdf.cell(40, 8, "Report Date:", 0, 0)
        pdf.cell(0, 8, data['created_at'].strftime('%Y-%m-%d'), 0, 1)

        pdf.ln(10)

    def _add_diagnosis_results(self, pdf: FPDF, data: Dict[str, Any]):
        """Add diagnosis results section"""
        pdf.set_font("Arial", "B", 14)
        pdf.cell(0, 12, "Diagnosis Results", 0, 1)
        pdf.ln(2)

        pdf.set_font("Arial", "", 11)
        prediction = data.get('prediction', {})

        # Main diagnosis
        pdf.set_font("Arial", "B", 12)
        pdf.cell(0, 10, "Primary Diagnosis:", 0, 1)
        pdf.set_font("Arial", "", 11)

        stage_desc = prediction.get('stage_description', 'Unknown')
        confidence = data.get('confidence', 0) * 100

        pdf.cell(0, 8, f"Stage: {prediction.get('stage', 'Unknown')}", 0, 1)
        pdf.cell(0, 8, f"Description: {stage_desc}", 0, 1)
        pdf.cell(0, 8, f"Confidence: {confidence:.1f}%", 0, 1)
        pdf.cell(0, 8, f"Risk Level: {prediction.get('risk_level', 'Unknown').title()}", 0, 1)

        pdf.ln(5)

        # Stage probabilities
        pdf.set_font("Arial", "B", 12)
        pdf.cell(0, 10, "Stage Probabilities:", 0, 1)
        pdf.set_font("Arial", "", 10)

        probabilities = prediction.get('probabilities', {})
        for stage, prob in probabilities.items():
            pdf.cell(0, 6, f"{stage.replace('_', ' ').title()}: {prob*100:.1f}%", 0, 1)

        pdf.ln(10)

    def _add_symptoms_analysis(self, pdf: FPDF, data: Dict[str, Any]):
        """Add symptoms analysis section"""
        pdf.set_font("Arial", "B", 14)
        pdf.cell(0, 12, "Symptoms Analysis", 0, 1)
        pdf.ln(2)

        pdf.set_font("Arial", "", 11)
        symptoms = data.get('symptoms', {})

        # Key symptoms
        key_symptoms = [
            ('Age', symptoms.get('age')),
            ('Sex', 'Male' if symptoms.get('sex') == 1 else 'Female'),
            ('Fatigue Level', symptoms.get('fatigue_level')),
            ('Alcohol Consumption', symptoms.get('alcohol_consumption')),
            ('Weight Loss (kg)', symptoms.get('weight_loss_kg')),
            ('Abdominal Swelling', 'Yes' if symptoms.get('abdominal_swelling') else 'No'),
            ('Appetite Loss', 'Yes' if symptoms.get('appetite_loss') else 'No'),
            ('Jaundice', 'Yes' if symptoms.get('jaundice') else 'No'),
            ('Fever', 'Yes' if symptoms.get('fever') else 'No'),
        ]

        for symptom, value in key_symptoms:
            pdf.cell(60, 8, f"{symptom}:", 0, 0)
            pdf.cell(0, 8, str(value), 0, 1)

        pdf.ln(5)

        # Lab values
        pdf.set_font("Arial", "B", 12)
        pdf.cell(0, 10, "Laboratory Values:", 0, 1)
        pdf.set_font("Arial", "", 10)

        lab_values = [
            ('Bilirubin', symptoms.get('bilirubin'), 'mg/dL'),
            ('Cholesterol', symptoms.get('cholesterol'), 'mg/dL'),
            ('Albumin', symptoms.get('albumin'), 'g/dL'),
            ('Copper', symptoms.get('copper'), 'ug/dL'),
            ('Alkaline Phosphatase', symptoms.get('alk_phos'), 'IU/L'),
            ('AST', symptoms.get('ast'), 'IU/L'),
            ('Triglycerides', symptoms.get('triglycerides'), 'mg/dL'),
            ('Platelets', symptoms.get('platelets'), 'x10^3/uL'),
            ('Prothrombin', symptoms.get('prothrombin'), 'seconds'),
        ]

        for test, value, unit in lab_values:
            if value is not None:
                pdf.cell(80, 6, f"{test}:", 0, 0)
                pdf.cell(0, 6, f"{value} {unit}", 0, 1)

        pdf.ln(10)

    def _add_recommendations(self, pdf: FPDF, data: Dict[str, Any]):
        """Add recommendations section"""
        pdf.set_font("Arial", "B", 14)
        pdf.cell(0, 12, "Recommendations", 0, 1)
        pdf.ln(2)

        pdf.set_font("Arial", "", 11)
        recommendations = data.get('recommendations', [])

        if recommendations:
            for i, rec in enumerate(recommendations, 1):
                pdf.cell(0, 8, f"{i}. {rec}", 0, 1)
        else:
            pdf.cell(0, 8, "No specific recommendations available.", 0, 1)

        pdf.ln(5)

        # Follow-up requirements
        prediction = data.get('prediction', {})
        if prediction.get('follow_up_required'):
            pdf.set_font("Arial", "B", 11)
            pdf.cell(0, 10, "Follow-up Required: Yes", 0, 1)
            pdf.set_font("Arial", "", 10)
            pdf.cell(0, 8, "Please schedule follow-up appointment with healthcare provider.", 0, 1)

        if prediction.get('specialist_referral'):
            pdf.set_font("Arial", "B", 11)
            pdf.cell(0, 10, "Specialist Referral Recommended: Yes", 0, 1)
            pdf.set_font("Arial", "", 10)
            pdf.cell(0, 8, "Referral to hepatologist or gastroenterologist advised.", 0, 1)

        pdf.ln(10)

    def _add_explanations(self, pdf: FPDF, data: Dict[str, Any]):
        """Add AI explanations section"""
        pdf.set_font("Arial", "B", 14)
        pdf.cell(0, 12, "AI Analysis Explanation", 0, 1)
        pdf.ln(2)

        pdf.set_font("Arial", "", 10)
        explanation = data.get('explanation', {})

        # Natural language explanation
        nl_explanation = explanation.get('natural_language_explanation', '')
        if nl_explanation:
            pdf.multi_cell(0, 6, nl_explanation)
            pdf.ln(5)

        # Key factors
        key_factors = explanation.get('key_factors', [])
        if key_factors:
            pdf.set_font("Arial", "B", 11)
            pdf.cell(0, 8, "Key Factors Considered:", 0, 1)
            pdf.set_font("Arial", "", 10)
            for factor in key_factors:
                pdf.cell(0, 6, f"- {factor}", 0, 1)
            pdf.ln(5)

        # Confidence interpretation
        confidence_interp = explanation.get('confidence_interpretation', '')
        if confidence_interp:
            pdf.set_font("Arial", "B", 11)
            pdf.cell(0, 8, "Confidence Assessment:", 0, 1)
            pdf.set_font("Arial", "", 10)
            pdf.multi_cell(0, 6, confidence_interp)

        pdf.ln(10)

    def _add_footer(self, pdf: FPDF):
        """Add report footer"""
        pdf.set_y(-30)
        pdf.set_font("Arial", "I", 8)
        pdf.cell(0, 5, self.footer_text, 0, 1, "C")

        pdf.cell(0, 5, "Generated by Explainable AI-Based Liver Cirrhosis Detection System", 0, 1, "C")

        # Page number
        pdf.cell(0, 5, f"Page {pdf.page_no()}", 0, 0, "C")

    async def generate_summary_report(self, user_id: str, start_date: datetime, end_date: datetime) -> str:
        """Generate summary report for multiple diagnoses"""
        try:
            # Get all diagnoses for user in date range
            diagnoses = await self._get_user_diagnoses(user_id, start_date, end_date)

            if not diagnoses:
                raise ValueError("No diagnosis records found for the specified period")

            # Create summary PDF
            pdf = self._create_summary_pdf(diagnoses, user_id, start_date, end_date)

            # Save PDF
            filename = f"summary_report_{user_id}_{start_date.strftime('%Y%m%d')}_{end_date.strftime('%Y%m%d')}.pdf"
            filepath = os.path.join("reports", filename)
            os.makedirs("reports", exist_ok=True)

            pdf.output(filepath)

            return filepath

        except Exception as e:
            logger.error(f"Summary report generation error: {str(e)}")
            raise

    async def _get_user_diagnoses(self, user_id: str, start_date: datetime, end_date: datetime) -> List[Dict[str, Any]]:
        """Get user's diagnoses within date range"""
        try:
            db = next(get_db())

            diagnoses = db.query(DiagnosisRecord).filter(
                DiagnosisRecord.user_id == user_id,
                DiagnosisRecord.created_at >= start_date,
                DiagnosisRecord.created_at <= end_date
            ).order_by(DiagnosisRecord.created_at.desc()).all()

            return [self._diagnosis_to_dict(d) for d in diagnoses]

        except Exception as e:
            logger.error(f"Diagnoses retrieval error: {str(e)}")
            return []

    def _diagnosis_to_dict(self, diagnosis: DiagnosisRecord) -> Dict[str, Any]:
        """Convert diagnosis record to dictionary"""
        return {
            'id': diagnosis.id,
            'created_at': diagnosis.created_at,
            'prediction': diagnosis.prediction,
            'confidence': diagnosis.confidence,
            'stage': diagnosis.prediction.get('stage') if diagnosis.prediction else 'Unknown'
        }

    def _create_summary_pdf(self, diagnoses: List[Dict[str, Any]], user_id: str, start_date: datetime, end_date: datetime) -> FPDF:
        """Create summary PDF report"""
        pdf = FPDF()
        pdf.add_page()

        # Header
        pdf.set_font("Arial", "B", 16)
        pdf.cell(0, 15, "Liver Health Summary Report", 0, 1, "C")
        pdf.ln(5)

        pdf.set_font("Arial", "", 10)
        pdf.cell(0, 8, f"Patient ID: {user_id}", 0, 1)
        pdf.cell(0, 8, f"Period: {start_date.strftime('%Y-%m-%d')} to {end_date.strftime('%Y-%m-%d')}", 0, 1)
        pdf.cell(0, 8, f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}", 0, 1)
        pdf.ln(10)

        # Summary statistics
        self._add_summary_stats(pdf, diagnoses)

        # Diagnosis timeline
        self._add_diagnosis_timeline(pdf, diagnoses)

        # Footer
        self._add_footer(pdf)

        return pdf

    def _add_summary_stats(self, pdf: FPDF, diagnoses: List[Dict[str, Any]]):
        """Add summary statistics"""
        pdf.set_font("Arial", "B", 14)
        pdf.cell(0, 12, "Summary Statistics", 0, 1)
        pdf.ln(2)

        pdf.set_font("Arial", "", 11)

        total_diagnoses = len(diagnoses)
        pdf.cell(0, 8, f"Total Assessments: {total_diagnoses}", 0, 1)

        if diagnoses:
            # Average confidence
            avg_confidence = sum(d['confidence'] for d in diagnoses) / total_diagnoses
            pdf.cell(0, 8, f"Average Confidence: {avg_confidence*100:.1f}%", 0, 1)

            # Stage distribution
            stages = [d['stage'] for d in diagnoses]
            stage_counts = {}
            for stage in stages:
                stage_counts[stage] = stage_counts.get(stage, 0) + 1

            pdf.ln(5)
            pdf.set_font("Arial", "B", 12)
            pdf.cell(0, 10, "Stage Distribution:", 0, 1)
            pdf.set_font("Arial", "", 10)

            for stage, count in stage_counts.items():
                percentage = (count / total_diagnoses) * 100
                pdf.cell(0, 6, f"{stage}: {count} assessments ({percentage:.1f}%)", 0, 1)

        pdf.ln(10)

    def _add_diagnosis_timeline(self, pdf: FPDF, diagnoses: List[Dict[str, Any]]):
        """Add diagnosis timeline"""
        pdf.set_font("Arial", "B", 14)
        pdf.cell(0, 12, "Assessment Timeline", 0, 1)
        pdf.ln(2)

        pdf.set_font("Arial", "", 10)

        for diagnosis in diagnoses:
            date_str = diagnosis['created_at'].strftime('%Y-%m-%d %H:%M')
            stage = diagnosis['stage']
            confidence = diagnosis['confidence'] * 100

            pdf.cell(50, 8, date_str, 0, 0)
            pdf.cell(40, 8, f"Stage: {stage}", 0, 0)
            pdf.cell(0, 8, f"Confidence: {confidence:.1f}%", 0, 1)

        pdf.ln(10)

# Global report generator instance
report_generator = MedicalReportGenerator()
